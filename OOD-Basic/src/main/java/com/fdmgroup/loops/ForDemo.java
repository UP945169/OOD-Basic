package com.fdmgroup.loops;

public class ForDemo {

	public static void main(String[] args) {
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}

		for (int i = 10; i >= 1; i--) {
			System.out.println(i);
		}

		for(char letter = 'a'; letter <= 'z'; letter ++){
			System.out.print(letter);
		}
	}

}
