package com.fdmgroup.conditionals;

public class SwitchCaseDemo {
	public static void main(String[] args) {
		int choice = 1;
		
		switch(choice) {
		case 1:
			System.out.println("a");
			break;
		case 2:
			System.out.println("b");
			break;
		case 3:
			System.out.println("c");
			break;
		case 4:
			System.out.println("d");
			break;
		default:
			System.out.println("x");
		}
	}
}
