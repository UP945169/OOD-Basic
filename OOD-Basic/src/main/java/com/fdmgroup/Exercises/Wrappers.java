package com.fdmgroup.Exercises;

public class Wrappers {
    public static void main(String[] args) {
        //1. Convert Strings to ints
        String number1 = "5";
        String number2 = "3";

        System.out.println(Integer.parseInt(number1));
        System.out.println(Integer.parseInt(number2));
        System.out.println(Integer.parseInt(number1) + Integer.parseInt(number2));

        //2. Convert Strings to doubles

        String number1for2 = "5.3";
        String number2for2 = "3.2";

        System.out.println(Double.parseDouble(number1for2));
        System.out.println(Double.parseDouble(number2for2));
        System.out.println(Double.parseDouble(number1for2) + Double.parseDouble(number2for2));

        //3. Extracting numbers from Strings

    }
}
